    private void loadLoaiPhimToComboBox() {
        loaiPhimList = new LoaiPhimDaoImpl().findAll();
        cboLoaiPhim.removeAllItems();
        for (LoaiPhim loai : loaiPhimList) {
            cboLoaiPhim.addItem(loai.getTenLoai()); // ✅ String
        }
    }

