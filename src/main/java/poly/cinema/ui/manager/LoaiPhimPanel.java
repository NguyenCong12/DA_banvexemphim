    @Override
    public LoaiPhim getForm() {
        String tenLoai = txtTheLoai.getText().trim();

        if (tenLoai.isEmpty()) {
            JOptionPane.showMessageDialog(this, "T√™n th·ªÉ lo·∫°i kh√¥ng ƒë∆∞·ª£c ƒë·ªÉ tr·ªëng!");
            return null;
        }

        Integer maLoai = null;

        return LoaiPhim.builder()
                .maLoai(maLoai)
                .tenLoai(tenLoai)
                .build();
    }

    @Override
    public void fillToTable() {

        DefaultTableModel model = (DefaultTableModel) tblLoaiPhim.getModel();
        model.setRowCount(0);

        // L·∫•y danh s√°ch v√† s·∫Øp x·∫øp theo m√£ lo·∫°i tƒÉng d·∫ßn
        List<LoaiPhim> danhSach = loaiPhimDao.findAll();
        danhSach.sort(Comparator.comparing(LoaiPhim::getMaLoai)); // S·∫Øp x·∫øp tƒÉng d·∫ßn theo m√£

        // ƒê·ªï d·ªØ li·ªáu v√†o b·∫£ng
        for (LoaiPhim loai : danhSach) {
            model.addRow(new Object[]{
                loai.getMaLoai(), loai.getTenLoai()
            });
        }
        updateButtonStatus();
    }

    @Override
    public void edit() {
        int row = tblLoaiPhim.getSelectedRow();
        if (row == -1) {
            return;
        }

        Integer maLoai = (Integer) tblLoaiPhim.getValueAt(row, 0);
        LoaiPhim loai = loaiPhimDao.findById(maLoai);
        if (loai != null) {
            setForm(loai);
        } else {
            JOptionPane.showMessageDialog(this, "Kh√¥ng t√¨m th·∫•y b·∫£n ghi n√†y trong CSDL!");
        }
    }

    @Override
    public void create() {
        String tenLoai = txtTheLoai.getText().trim();

        // üîí Ki·ªÉm tra kh√¥ng ƒë·ªÉ tr·ªëng
        if (tenLoai.isEmpty()) {
            JOptionPane.showMessageDialog(this, "T√™n th·ªÉ lo·∫°i kh√¥ng ƒë∆∞·ª£c ƒë·ªÉ tr·ªëng.");
            return;
        }

        // üîí Ki·ªÉm tra tr√πng t√™n (kh√¥ng ph√¢n bi·ªát hoa th∆∞·ªùng)
        LoaiPhim existing = loaiPhimDao.findByName(tenLoai);
        if (existing != null) {
            JOptionPane.showMessageDialog(this, "T√™n th·ªÉ lo·∫°i ƒë√£ t·ªìn t·∫°i.");
            return;
        }

        // ‚úÖ T·∫°o ƒë·ªëi t∆∞·ª£ng m·ªõi (gi·∫£ s·ª≠ ID t·ª± tƒÉng)
        LoaiPhim entity = new LoaiPhim(null, tenLoai);
        try {
            LoaiPhim created = loaiPhimDao.create(entity);
            if (created != null) {
                JOptionPane.showMessageDialog(this, "Th√™m th√†nh c√¥ng!");
                fillToTable();
                updateButtonStatus();
                clear();
            } else {
                JOptionPane.showMessageDialog(this, "Th√™m th·∫•t b·∫°i!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "L·ªói khi th√™m: " + e.getMessage());
            e.printStackTrace();
        }
    }

    @Override
public void update() {
    int row = tblLoaiPhim.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Vui l√≤ng ch·ªçn m·ªôt b·∫£n ghi ƒë·ªÉ s·ª≠a.");
        return;
    }

    Integer maLoai = (Integer) tblLoaiPhim.getValueAt(row, 0);
    String tenLoai = txtTheLoai.getText().trim();

    // üîí Ki·ªÉm tra r·ªóng
    if (tenLoai.isEmpty()) {
        JOptionPane.showMessageDialog(this, "T√™n th·ªÉ lo·∫°i kh√¥ng ƒë∆∞·ª£c ƒë·ªÉ tr·ªëng.");
        return;
    }

    // üîÑ L·∫•y b·∫£n g·ªëc t·ª´ CSDL
    LoaiPhim old = loaiPhimDao.findById(maLoai);
    if (old == null) {
        JOptionPane.showMessageDialog(this, "Kh√¥ng t√¨m th·∫•y th·ªÉ lo·∫°i ƒë·ªÉ c·∫≠p nh·∫≠t.");
        return;
    }

    // üîç Ki·ªÉm tra kh√¥ng c√≥ thay ƒë·ªïi
    if (old.getTenLoai().equalsIgnoreCase(tenLoai)) {
        JOptionPane.showMessageDialog(this, "B·∫°n ch∆∞a thay ƒë·ªïi th√¥ng tin n√†o ƒë·ªÉ c·∫≠p nh·∫≠t.");
        return;
    }

    // üîí Ki·ªÉm tra tr√πng t√™n v·ªõi lo·∫°i kh√°c
    LoaiPhim existed = loaiPhimDao.findByName(tenLoai);
    if (existed != null && !existed.getMaLoai().equals(maLoai)) {
        JOptionPane.showMessageDialog(this, "T√™n th·ªÉ lo·∫°i ƒë√£ t·ªìn t·∫°i.");
        return;
    }

    // ‚úÖ C·∫≠p nh·∫≠t
    LoaiPhim entity = LoaiPhim.builder()
            .maLoai(maLoai)
            .tenLoai(tenLoai)
            .build();

    try {
        loaiPhimDao.update(entity);
        JOptionPane.showMessageDialog(this, "C·∫≠p nh·∫≠t th√†nh c√¥ng!");
        fillToTable();
        clear();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "L·ªói khi c·∫≠p nh·∫≠t: " + e.getMessage());
        e.printStackTrace();
    }
}


    @Override
    public void delete() {
        int row = tblLoaiPhim.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui l√≤ng ch·ªçn m·ªôt b·∫£n ghi ƒë·ªÉ xo√°.");
            return;
        }

        Integer maLoai = (Integer) tblLoaiPhim.getValueAt(row, 0);
        int confirm = JOptionPane.showConfirmDialog(this, "B·∫°n ch·∫Øc ch·∫Øn mu·ªën x√≥a?", "X√°c nh·∫≠n", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            loaiPhimDao.deleteById(maLoai);
            JOptionPane.showMessageDialog(this, "X√≥a th√†nh c√¥ng!");
            fillToTable();
            clear();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "X√≥a th·∫•t b·∫°i: ");
            e.printStackTrace();
        }
    }

